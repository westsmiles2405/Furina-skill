/** 根据时间段返回合适的问候 */
export function getTimeBasedGreeting(): string {
    const hour = new Date().getHours();
    if (hour < 6) {
        return '这么晚还在？哼，虽然本大导演也在……但你真的该休息了。要不先完成手头这一小段，然后去睡吧。';
    } else if (hour < 12) {
        return getOpening();
    } else if (hour < 14) {
        return '午后了？别忘了吃饭。一个饿着肚子的演员可演不出好戏。先补充能量，再继续吧。';
    } else if (hour < 18) {
        return '下午好。这个时间段最适合全力冲刺了。来吧，让我们把今天最重要的一幕拿下！';
    } else {
        return '晚上了呢。今天的演出进展如何？如果还剩收尾工作，赶紧处理，别拖到深夜。';
    }
}

